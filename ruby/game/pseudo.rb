=begin
	
User 1 enters word or phrase - 
Word/Phrase gets stored as the answer - 
- Numer of guesses is related to the length of the answer - 
- Length of word is displayed as blanks - 
User 2 guesses a letter - 
- If letter matches a letter in answer, game continues and display the letter inline with undiscovered letters - 
-- Every time another correct letter is guessed, get added to already guessed ones - 
- If letter doesn't match any letters in answer, game continues and tell User 2 they are incorrect - 
Game ends when
- User 2 uses up all guesses
- User 2 fills in all letters
=end